    public int getmax(int[] l, int[] t) {
        // use determine critical pts
        int n = l.length;
        ArrayList<Integer> pts = new ArrayList<Integer>();
        for (int i = 0; i < n; i++) {
            pts.add(t[i]);
            pts.add(t[i] + l[i]);
        }

        // make bitsets for each critical pt, eels able to get cached
        BitSet[] cache = new BitSet[pts.size()];
        for (int i = 0; i < pts.size(); i++) {
            int curr = pts.get(i);
            cache[i] = new BitSet();
            for (int j = 0; j < n; j++) {
                if (curr >= t[j] && curr <= t[j] + l[j]) {
                    cache[i].set(j);
                }
            }
        }

        // iterate through pairs, find the best
        int ret = 0;
        for (int i = 0; i < pts.size(); i++) {
            for (int j = i; j < pts.size(); j++) {
                BitSet s = cache[i].get(0, cache[i].length());
                s.or(cache[j]);
                ret = Math.max(ret, s.cardinality());
            }
        }
        return ret;
    }

