    public String getContainerInfo(String containers, int[] reports, int L) {
        // get count from container layout
        int[] count = new int[containers.length() - L + 1];
        for (int i = 0; i < count.length; i++) {
            for (int j = 0; j < L; j++) {
                if (containers.charAt(i + j) == 'X')
                    count[i]++;
            }
        }

        // count reports
        int[] qty = new int[L + 1];
        for (int i : reports) {
            qty[i]++;
        }

        char[] answer = new char[containers.length()];
        Arrays.fill(answer, '-');
        int[] covered = new int[containers.length()];

        // all lengths
        for (int i = 0; i <= L; i++) {
            if (qty[i] == 0) continue;

            Arrays.fill(covered, 0);
            int count_i_total = 0;
            for (int j = 0; j < count.length; j++) {
                if (count[j] == i) {
                    for (int k = 0; k < L; k++)
                        covered[j + k]++;
                    count_i_total++;
                }
            }
            for (int j = 0; j < covered.length; j++) {
                if (covered[j] != 0 && answer[j] == '-')
                    answer[j] = '?';
                if (covered[j] > count_i_total - qty[i])
                    answer[j] = '+';
            }
        }
        return new String(answer);
    }

