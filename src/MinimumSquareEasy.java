    public long minArea(int[] x, int[] y) {
        long ret = Long.MAX_VALUE;
        int n = x.length;
        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                long minX, minY, maxX, maxY;
                minX = minY = Long.MAX_VALUE;
                maxX = maxY = Long.MIN_VALUE;
                for (int k = 0; k < n; k++) {
                    if (k == i || k == j) continue;
                    minX = Math.min(minX, x[k]);
                    minY = Math.min(minY, y[k]);
                    maxX = Math.max(maxX, x[k]);
                    maxY = Math.max(maxY, y[k]);
                }
                long size = Math.max((maxX - minX + 2) , (maxY - minY + 2));
                if (1.0 * size * size < ret) ret = size * size;
            }
        }
        return ret;
    }

