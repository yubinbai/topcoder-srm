    static {
        int a = 0, b = 1, temp = 0;
        fibs.add(a);
        while (a <= 2000000) {
            temp = b;
            b = a + b;
            a = temp;
            fibs.add(temp);
        }
    }
    public int find(int N)
    {
        int pos = Collections.binarySearch(fibs, N);
        if (pos >= 0) {
            return 0;
        } else {
            pos = -pos -1;
        }

        return Math.min(Math.abs(N - fibs.get(pos - 1)), Math.abs(N - fibs.get(pos)));

    }

