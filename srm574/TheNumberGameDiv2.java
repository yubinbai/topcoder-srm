	public int minimumMoves(int A, int B)
	{
		ArrayDeque<String> q = new ArrayDeque<String>();
        HashMap<String, Integer> seen = new HashMap<String, Integer>();
        int ret = 100;
        q.push(String.format("%d", A));
        seen.put(String.format("%d", A), 0);
        while (!q.isEmpty()) {
            String curr = q.poll();
            int dist = seen.get(curr);
            if (curr.equals(String.format("%d", B))) {
                ret = Math.min(ret, dist);
            }

            String cut = curr.substring(0, Math.max(0, curr.length() - 1));
            if (!seen.containsKey(cut)) {
                seen.put(cut, dist + 1);
                q.push(cut);
            } else {
                int prev_dist = seen.get(cut);
                if (prev_dist > dist + 1) {
                    seen.put(cut, dist + 1);
                    q.push(cut);
                }
            }
            String rev = new StringBuffer(curr).reverse().toString();
            if (!seen.containsKey(rev)) {
                seen.put(rev, dist + 1);
                q.push(rev);
            } else {
                int prev_dist = seen.get(rev);
                if (prev_dist > dist + 1) {
                    seen.put(rev, dist + 1);
                    q.push(rev);
                }
            }
        }
        return ret == 100 ? -1 : ret;
	}


