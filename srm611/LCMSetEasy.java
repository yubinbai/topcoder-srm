    public String include(int[] S, int x) {
        int n = S.length;
        boolean[] inc = new boolean[n];
        for (int i = 0; i < n; i++) {
            if (x % S[i] == 0) inc[i] = true;
        }
        int lcm = 1;
        for (int i = 0; i < n; i++) {
            if (inc[i]) {
                lcm = lcm(lcm, S[i]);
            }
        }

        return (lcm == x) ? "Possible" : "Impossible";

    }

    public int lcm(int a, int b) {
        return (int) (1L * a * b / gcd(a, b));
    }

    public int gcd(int a, int b) {
        if (b == 0) return a;
        return gcd(b, a % b);
    }

