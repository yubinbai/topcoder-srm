    public int grundyNumber(int pos) {
        if (grundy[pos] != -1) return grundy[pos];
        List<Integer> moves = divisors(pos);
        HashSet<Integer> set = new HashSet<Integer>();
        for (int x : moves) {
            set.add(grundyNumber(pos - x));
        }
        //return the smallest non-negative integer not in the set s.
        int ret=0;
        while (set.contains(ret)) ret++;
        return grundy[pos] = ret;
    }

    public static List<Integer> divisors(int number) {
        int n = number;
        List<Integer> factors = new ArrayList<Integer>();
        for (int i = 2; i < n; i++) {
            if (n % i == 0) {
                factors.add(i);
            }
        }
        return factors;
    }
