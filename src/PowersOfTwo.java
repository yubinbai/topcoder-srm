    public long count(long[] powers) {
        for (long p : powers ) {
            for (int i = 0; i < 51; ++i) {
                if ( ((1L << i) & p) != 0 ) cnt[i]++;
            }
        }
        return recur(0);
    }

    private long recur(int step) {
        if (step == 51) return (cnt[51] == 1) ? 2L : 1L;
        if (cnt[step] == 0) return recur(step + 1);
        if (cnt[step] == 1) return recur(step + 1) * 2L;
        long ret = 0;

        cnt[step + 1] += cnt[step] / 2;
        ret += recur(step + 1);
        cnt[step + 1] -= cnt[step] / 2;

        cnt[step + 1] += (cnt[step] - 1) / 2;
        ret += recur(step + 1);
        cnt[step + 1] -= (cnt[step] - 1) / 2;

        return ret;
    }


